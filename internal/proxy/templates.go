type maintenanceData struct {
	Domain  string
	HTMLDir string
	HasSSL  bool
}

var nginxTmpl *template.Template
var maintenanceTmpl *template.Template

func init() {
	nginxTmpl = template.Must(template.New("nginx").Parse(nginxConfTemplate))
	maintenanceTmpl = template.Must(template.New("maintenance").Parse(maintenanceConfTemplate))
}
